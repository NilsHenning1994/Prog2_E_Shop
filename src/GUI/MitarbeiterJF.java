    private void jButtonEventAktActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEventAktActionPerformed
        jListEvent.removeAll();
        DefaultListModel listModel = new DefaultListModel();
        for(int i = 0; i<eventliste.size(); i++){

            try {
                switch(eventliste.get(i).getTyp()){
                    case HINZUGEFUEGT:
                    {
                        listModel.addElement(eventliste.get(i).getPerson().getName() + " hat "+ eventliste.get(i).getArtikel().getBez() +
                            " am " + eventliste.get(i).getDate() + " hinzugefügt.");

                        break;
                    }

                    case EINGELAGERT:
                    {

                        listModel.addElement(eventliste.get(i).getPerson().getName() + " hat "+ eventliste.get(i).getArtikel().getBez() +
                            " am " + eventliste.get(i).getDate() + " eingelagert.");
                        break;
                    }

                    case AUSGELAGERT:
                    {

                        listModel.addElement(eventliste.get(i).getPerson().getName() + " hat "+ eventliste.get(i).getArtikel().getBez() +
                            " am " + eventliste.get(i).getDate() + " ausgelagert.");
                        break;
                    }
                    case KAUF:
                    {
                        listModel.addElement(eventliste.get(i).getPerson().getName() + " hat "+ eventliste.get(i).getArtikel().getBez() +
                            " am " + eventliste.get(i).getDate() + " " + eventliste.get(i).getAnzahl() + " mal gekauft.");
                        break;

                    }
                    case GELOESCHT:
                    {
                        listModel.addElement(eventliste.get(i).getPerson().getName() + " hat "+ eventliste.get(i).getArtikel().getBez() +
                            " am " + eventliste.get(i).getDate() + " gelöscht");

                        break;
                    }

                }   } catch (RemoteException ex) {
                    Logger.getLogger(MitarbeiterJF.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

            jListEvent.setModel(listModel);
    }//GEN-LAST:event_jButtonEventAktActionPerformed

