    private void jButtonLFSuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLFSuchenActionPerformed
        // listLVsuchErg.clear();
        model = new DefaultTableModel();
        jTableLV.setModel(model);
        model.addColumn("Artnr");
        model.addColumn("Bezeichnung");
        model.addColumn("Preis");
        model.addColumn("Bestand");
        jTextFieldLVartnrb.setText("");
        jTextFieldLVbezb.setText("");
        jTextFieldLVPreisb.setText("");
        jTextFieldLVBestandb.setText("");

        if(jTextFieldLFArtSuchen.getText().equalsIgnoreCase("")){

            try {
                List<ArtikelRemote> suchErgebnis = eshopartikel.gibArtikelListe();

                for (ArtikelRemote artikel: suchErgebnis) {
                    //model.addRow(new Object[]{artikel.getNummer(), artikel.getBez(), artikel.getPreis(), 0});
                    model.addRow(new Object[]{artikel.getNummer(), artikel.getBez(), artikel.getPreis(), bestandMap.get(Integer.parseInt(artikel.getNummer()))});
                }

            } catch (IOException ex) {
                Logger.getLogger(MitarbeiterJF.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            artnrString = "";
            bez = "";
            preis = "";
            bestandString = "";
            try {
                //            s = "" + eshopartikel.sucheNachArtikeltitel(jTextFieldLFArtSuchen.getText());
                Vector<ArtikelRemote> suchErgebnis = eshopartikel.sucheNachArtikeltitel(jTextFieldLFArtSuchen.getText());
                for (ArtikelRemote artikel: suchErgebnis) {
                    // model.addRow(new Object[]{artikel.getNummer(), artikel.getBez(), artikel.getPreis(), 0});
                    model.addRow(new Object[]{artikel.getNummer(), artikel.getBez(), artikel.getPreis(), bestandMap.get(Integer.parseInt(artikel.getNummer()))});

                }
            } catch (IOException ex) {
                Logger.getLogger(MitarbeiterJF.class.getName()).log(Level.SEVERE, null, ex);
                jLabelSuchenError.setForeground(Color.RED);
                jLabelSuchenError.setText("Keine Artikel gefunden!");
            }

        }
    }//GEN-LAST:event_jButtonLFSuchenActionPerformed

