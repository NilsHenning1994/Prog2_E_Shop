    private void jButtonLVartanlegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLVartanlegenActionPerformed

        String nr = jTextFieldLVartnr.getText();
        String bez = jTextFieldLVbez.getText() + " " + jTextFieldLVMasse.getText() + " " + jComboBoxLV.getSelectedItem().toString();
        String preis = jTextFieldLVPreis.getText();
        String bestand = "0";
        try {
            eshopartikel.artikelAnlegen(nr, bez, preis);
            eshopartikel.schreibeArtikel();
            jLabelLFArtanExcep.setForeground(Color.GREEN);
            jLabelLFArtanExcep.setText("Artikel wurde angelegt!");
            jButtonLVneuanlegen.setEnabled(true);
            jButtonLVartanlegen.setEnabled(false);
            jTextFieldLVartnr.setEnabled(false);
            jTextFieldLVbez.setEnabled(false);
            jTextFieldLVPreis.setEnabled(false);

        } catch (ArtikelExistiertBereitsException e) {
            // Hier Fehlerbehandlung...
            jLabelLFArtanExcep.setForeground(Color.RED);
            jLabelLFArtanExcep.setText(e.getMessage());

        } catch (RemoteException ex) {
            Logger.getLogger(MitarbeiterJF.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(MitarbeiterJF.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonLVartanlegenActionPerformed

