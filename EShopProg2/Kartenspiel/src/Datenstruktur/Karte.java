package Datenstruktur;

public class Karte {
	private int zahl;
	private int color;
	public Karte(int zahl, int color) {
		super();
		this.zahl = zahl;
		this.color = color;
	}
	public int getZahl() {
		return zahl;
	}
	public void setZahl(int zahl) {
		this.zahl = zahl;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	
	
	

}
